    private void updateProfileBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateProfileBtnActionPerformed
        String email = emailField.getText();
        String password = passwordField.getText();
        String name = nameField.getText();

        User userUpdated = userDao.get(this.userLoggedIn.getLogin());
        userUpdated.setEmail(email);
        userUpdated.setPassword(password);
        userUpdated.setName(name);
        userDao.update(userUpdated);

        JOptionPane.showMessageDialog(
                rootPane,
                "Usuario actualizado correctamente",
                "Exito",
                JOptionPane.INFORMATION_MESSAGE
        );

        this.dispose();
    }//GEN-LAST:event_updateProfileBtnActionPerformed

    private void deleteProfileBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteProfileBtnActionPerformed
        int userConfirmation = JOptionPane.showConfirmDialog(
                this,
                "Estas seguro que deseas eliminar tu cuenta?",
                "Eliminacion de usuario", JOptionPane.WARNING_MESSAGE);

        if (userConfirmation == 0) {
            userDao.delete(this.userLoggedIn.getLogin());
            JOptionPane.showMessageDialog(
                    this,
                    "Usuario eliminado correctamente!",
                    "Exito",
                    JOptionPane.INFORMATION_MESSAGE
            );

            this.dispose();

            if (this.mainWindow != null) {
                // Significa que esta ventana se abrio desde el main window (dashboard de usuario)
                this.mainWindow.dispose();
            }

            if (this.panelAdmin != null) {
                // Significa que esta ventana se abrio desde el panel de administrador
                this.panelAdmin.dispose();
            }
        }
    }//GEN-LAST:event_deleteProfileBtnActionPerformed

