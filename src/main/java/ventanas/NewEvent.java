    private void setComboboxModelOnInstance() {
        localComboBox.setModel(localComboBoxModel);
        visitComboBox.setModel(visitComboBoxModel);

        EquiposDAO equiposDao = new EquiposDAO();
        allTeams = equiposDao.obtenerTodosLosEquipos();

        localComboBoxModel.addAll(allTeams);
        visitComboBoxModel.addAll(allTeams);
    }

    private void settingListenersForComboboxes() {
        // Listener para el COMBO LOCAL
        localComboBox.addActionListener(e -> {
            // Obtenemos el equipo local que acaba de ser seleccionado
            Equipo equipoLocalSeleccionado = (Equipo) localComboBox.getSelectedItem();
            restartLocalTeams.setEnabled(true);
            visitComboBox.setEnabled(true);
            localComboBox.setEnabled(false);
            updateVisitCombobox(equipoLocalSeleccionado);
        });
    }

    /**
     * Actualiza el modelo del ComboBox de Visita. Quita el equipo que fue
     * seleccionado como Local.
     */
    private void updateVisitCombobox(Equipo equipoLocalSeleccionado) {
        visitComboBoxModel.removeAllElements();

        // Llenar el modelo de visita con todos los equipos...
        for (int i = 0; i < allTeams.size(); i++) {
            if (allTeams.get(i).getIdEquipo() != equipoLocalSeleccionado.getIdEquipo()) {
                visitComboBoxModel.addElement(allTeams.get(i));
            }
        }
    }

