    private void saveEventBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveEventBtnActionPerformed
        int day = (int) dayTxt.getValue();
        int month = (int) monthTxt.getValue();
        int year = (int) yearTxt.getValue();
        int startTimeHour = (int) this.startTimeHour.getValue();
        int startTimeMinute = (int) this.startTimeMinute.getValue();
        int endTimeHour = (int) this.endTimeHour.getValue();
        int endTimeMinute = (int) this.endTimeMinute.getValue();

        Calendar calendarDate = Calendar.getInstance();
        calendarDate.set(year, month, day);

        Calendar calendarStartTime = Calendar.getInstance();
        calendarStartTime.set(Calendar.HOUR_OF_DAY, startTimeHour);
        calendarStartTime.set(Calendar.MINUTE, startTimeMinute);
        calendarStartTime.set(Calendar.SECOND, 0);
        calendarStartTime.set(Calendar.MILLISECOND, 0);

        Calendar calendarEndTime = Calendar.getInstance();
        calendarEndTime.set(Calendar.HOUR_OF_DAY, endTimeHour);
        calendarEndTime.set(Calendar.MINUTE, endTimeMinute);
        calendarEndTime.set(Calendar.SECOND, 0);
        calendarEndTime.set(Calendar.MILLISECOND, 0);

        Date dateEventForDB = new Date(calendarDate.getTimeInMillis());
        Time startTime = new Time(calendarStartTime.getTimeInMillis());
        Time endTime = new Time(calendarEndTime.getTimeInMillis());
        String eventState = "habilitado";
        Equipo localTeamSelected = (Equipo) localComboBox.getSelectedItem();
        Equipo visitTeamSelected = (Equipo) visitComboBox.getSelectedItem();
        
        System.out.println("Local ID: " + localTeamSelected.getIdEquipo());
        System.out.println("Visit ID: " + visitTeamSelected.getIdEquipo());
        System.out.println("Equipo local: " + localTeamSelected.getNombre() + "vs Equipo Visita: " + visitTeamSelected.getNombre());
        
        
        EncuentroDAO encuentroDao = new EncuentroDAO();
        
        encuentroDao.agregarEncuentro(
                dateEventForDB,
                startTime,
                endTime,
                eventState,
                localTeamSelected.getIdEquipo(),
                visitTeamSelected.getIdEquipo()
        );
        
        this.panelAdmin.setEventsIntoEventListModel();
        this.dispose();

    }//GEN-LAST:event_saveEventBtnActionPerformed

